      const Month = ['Jan' ,'Feb' ,'Mar' ,'Apr' ,'May' ,'June', 'Jul' ,'Aug' ,'Sep' ,'Oct' ,'Nov' ,'Dec'];
      
    var date = new Date();
    
    var year = date.getFullYear();
    var month = date.getMonth();
    var datesday = date.getDate();
    var d = date.getDay();
    var h = date.getHours(); // 0 - 23
    var m = date.getMinutes(); // 0 - 59
    var s = date.getSeconds(); // 0 - 59
    var session = "AM";
    
    if(h == 0){
        h = 12;
    }
    
    if(h > 12){
        h = h - 12;
        session = "PM";
    }
    year = (year < 10) ? "0" + year : year;
    month = (month < 10) ? "0" + month : month;
    datesday = (datesday < 10) ? "0" + datesday : datesday;
    d = (d < 10) ? "0" + d : d;

    h = (h < 10) ? "0" + h : h;
    m = (m < 10) ? "0" + m : m;
    s = (s < 10) ? "0" + s : s;
    
 var dateday = dayNames[parseInt(d)] + "\n" + Month[parseInt(month)] + "/" + datesday + "/" + year;
    var time = h + ":" + m + ":" + s + " " + session;
 
    document.getElementById("dateday").innerText = dateday;
    document.getElementById("MyClockDisplay").innerText = time;
    document.getElementById("MyClockDisplay").textContent = time;
    
    setTimeout(showTime, 1000);
    
}

